    private void jtbukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbukuMouseClicked
        // TODO add your handling code here:
        if(jtbuku.getSelectedColumnCount() == 1){
            int rows = jtbuku.getSelectedRow();
            EDISBN.setText(jtbuku.getValueAt(rows, 0).toString());
            EDJudul.setText(jtbuku.getValueAt(rows, 1).toString());
            EDHarga.setText(jtbuku.getValueAt(rows, 8).toString());
            EDTahun.setText(jtbuku.getValueAt(rows, 9).toString());
            EDStok.setText(jtbuku.getValueAt(rows, 10).toString());
            EDidPenerbit.setText(jtbuku.getValueAt(rows, 4).toString());
            EDNamaPenerbit.setText(jtbuku.getValueAt(rows, 5).toString());
            EDIdPengarang.setText(jtbuku.getValueAt(rows, 2).toString());
            EDNamaPengarang.setText(jtbuku.getValueAt(rows, 3).toString());
            EDIdKategori.setText(jtbuku.getValueAt(rows, 6).toString());
            EDNamaKategori.setText(jtbuku.getValueAt(rows, 7).toString());
            
            EDISBN.setEnabled(false);
        }
    }//GEN-LAST:event_jtbukuMouseClicked

