    void isitabel(){
        Object data[][] = new Object[record.size()][11];
        int x = 0;
        for(MBuku20312067 buku : record){
            data[x][0] = buku.getISBN20312067();
            data[x][1] = buku.getJudul20312067();
            data[x][2] = buku.getIDPengarang20312067();
            data[x][3] = buku.getNamaPengarang20312067();
            data[x][4] = buku.getIDPenerbit20312067();
            data[x][5] = buku.getNamaPenerbit20312067();
            data[x][6] = buku.getIDKategori20312067();
            data[x][7] = buku.getNamaKategori20312067();
            data[x][8] = buku.getHarga20312067();
            data[x][9] = buku.getTahun20312067();
            data[x][10] = buku.getStok20312067();
            x++;
        }
        String judul[] = {"ISBN", "Judul", "ID Pengarang", "Nama Pengarang", "ID Penerbit", "Nama Penerbit", "ID Kategori", "Nama Kategori", "Harga", "Tahun", "Stok"};
        jtbuku.setModel(new DefaultTableModel(data, judul));
    }
    
